def parse_output_to_dict(output: Optional[str], criteria: List[str]) -> Dict[str, str]:
    result = {criterion: 'N/A' for criterion in criteria}
    if output is None:
        return result
    for line in output.strip().split('\n'):
        try:
            criterion, value = line.split(':', 1)
            criterion = criterion.replace('- ', '').strip()
            value = value.strip()
            if criterion in criteria and value in ('0', '1', 'N/A'):
                result[criterion] = value
        except (ValueError, IndexError):
            continue
    return result

