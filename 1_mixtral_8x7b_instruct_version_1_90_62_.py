def validate_dataset(df, required_columns, critical_columns=['Problem']):
    missing_critical = [col for col in critical_columns if col not in df.columns]
    if missing_critical:
        raise ValueError(f"❌ Missing critical columns: {missing_critical}")
    missing_non_critical = [col for col in required_columns if col not in df.columns and col not in critical_columns]
    if missing_non_critical:
        logging.warning(f"⚠️ Dataset missing non-critical columns: {missing_non_critical}")
    check_columns = [col for col in ['Problem', 'Grade', 'Question Type'] if col in df.columns]
    if check_columns:
        df = df.dropna(subset=check_columns)
    return df


