def make_row(
    case: Dict[str, str],
    zero: Dict[str, str],
    one: Dict[str, str],
    three: Dict[str, str],
    criteria: List[str]
) -> Dict[str, str]:
    row = {
        'Problem': case['problem'],
        'Grade': case['grade'],
        'Question Type': case['question_type']
    }
    for c in criteria:
        row[f'Zero-Shot_{c}'] = zero[c]
        row[f'One-Shot_{c}'] = one[c]
        row[f'Three-Shot_{c}'] = three[c]
    return row


