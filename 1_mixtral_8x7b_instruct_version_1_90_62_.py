def fine_tune_model(model, tokenizer, tokenized_train, tokenized_val):
    """Fine-tune the model with LoRA configuration."""
    lora_config = LoraConfig(
        r=8,
        lora_alpha=32,
        target_modules=["q_proj", "k_proj", "v_proj", "o_proj"],
        lora_dropout=0.05,
        bias="none",
        task_type="CAUSAL_LM"
    )

    model = get_peft_model(model, lora_config)
    model.config.use_cache = False
    model.train()

    for name, param in model.named_parameters():
        if "lora" in name:
            param.requires_grad_(True)
        else:
            param.requires_grad_(False)
    print(f"Trainable parameters: {sum(p.numel() for p in model.parameters() if p.requires_grad)}")

    data_collator = DataCollatorForLanguageModeling(tokenizer=tokenizer, mlm=False)

    training_args = TrainingArguments(
        output_dir="./results",
        num_train_epochs=1,
        per_device_train_batch_size=1,
        gradient_accumulation_steps=4,
        optim="adamw_8bit",
        learning_rate=2e-5,
        warmup_steps=500,
        bf16=True,
        save_steps=500,
        logging_steps=10,
        eval_strategy="steps",
        eval_steps=100,
        save_total_limit=5,
        load_best_model_at_end=True,
        metric_for_best_model="loss",
        report_to="none",
    )

    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=tokenized_train,
        eval_dataset=tokenized_val,
        data_collator=data_collator,
    )

    trainer.train()

    adapter_path = "./fine_tuned_mixtral-8x7b-instruct"
    os.makedirs(adapter_path, exist_ok=True)

    model.save_pretrained(adapter_path)
    tokenizer.save_pretrained(adapter_path)

    print("Files in adapter_path:", os.listdir(adapter_path))
    print(f"Model saved: {adapter_path}")

    # return model, tokenizer
    return model, tokenizer, trainer


# Training Loss Visualization
def visualize_training_loss(trainer):
    """Visualize training and validation loss."""
    log_history = trainer.state.log_history
    train_loss = [log['loss'] for log in log_history if 'loss' in log]
    val_loss = [log['eval_loss'] for log in log_history if 'eval_loss' in log]
    train_steps = [log['step'] for log in log_history if 'loss' in log]
    val_steps = [log['step'] for log in log_history if 'eval_loss' in log]

    if len(train_steps) > len(train_loss):
        train_steps = train_steps[:len(train_loss)]

    plt.figure(figsize=(12, 6), dpi=300)
    plt.plot(train_steps, train_loss, label='Training Loss')
    plt.plot(val_steps, val_loss, label='Validation Loss')
    plt.xlabel('Step')
    plt.ylabel('Loss')
    plt.legend()
    plt.grid(True)
    plt.show()

