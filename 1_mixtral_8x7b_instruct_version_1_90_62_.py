def timer(func):
    def wrapper(*args, **kwargs):
        start = time.time()
        result = func(*args, **kwargs)
        end = time.time()
        print(f"⏱️ '{func.__name__}' execution time: {end - start:.2f} seconds")
        return result
    return wrapper

