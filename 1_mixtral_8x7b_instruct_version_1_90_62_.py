def compare_results():
    # Load human evaluation results
    df_human_evaluation = pd.read_excel("MWP evaluation sheet.xlsx")

    # Rename "Section" to "Question Type" if needed
    if 'Section' in df_human_evaluation.columns and 'Question Type' not in df_human_evaluation.columns:
        df_human_evaluation.rename(columns={'Section': 'Question Type'}, inplace=True)

    # Load fine-tuned model evaluation results
    df_evaluation_fine_tuned = pd.read_excel("df_evaluation_fine_tuned.xlsx")

    # Dynamically determine common criteria
    human_columns = set(df_human_evaluation.columns)
    ft_columns = set(df_evaluation_fine_tuned.columns)
    common_criteria = [col for col in Config.CRITERIA if col in human_columns and col in ft_columns]
    console.print(f"[bold green]Common criteria: {common_criteria}[/bold green]")

    # Validate datasets using common criteria
    df_human_evaluation = validate_dataset(df_human_evaluation, ['Problem'] + common_criteria)
    df_evaluation_fine_tuned = validate_dataset(df_evaluation_fine_tuned, ['Problem'] + common_criteria + ['RawOutput'])

    # Data preprocessing: Clean Problem column
    df_human_evaluation['Problem'] = df_human_evaluation['Problem'].str.strip()
    df_evaluation_fine_tuned['Problem'] = df_evaluation_fine_tuned['Problem'].str.strip()

    # Merge datasets
    df_merged = pd.merge(
        df_human_evaluation,
        df_evaluation_fine_tuned,
        on='Problem',
        how='inner',
        suffixes=('_human', '_ft'))
    console.print(f"[bold green]Merged dataset contains {len(df_merged)} problems[/bold green]")

    # Verify columns to compare
    columns_to_compare = common_criteria
    available_columns = [col for col in columns_to_compare
                        if f"{col}_human" in df_merged.columns and f"{col}_ft" in df_merged.columns]
    console.print(f"[bold green]Comparable criteria: {available_columns}[/bold green]")

    # Calculate per-problem accuracy and discrepancies
    problem_accuracy = []
    total_correct = 0
    total_comparisons = 0
    discrepancies = []
    na_counts = {col: 0 for col in available_columns}

    for idx, row in df_merged.iterrows():
        correct_count = 0
        for col in available_columns:
            human_val = row[f"{col}_human"]
            ft_val = row[f"{col}_ft"]
            # Check if RawOutput_ft exists
            raw_model_output = row.get('RawOutput_ft', 'N/A')

            # Handle N/A and null values
            if pd.isna(human_val) or human_val == 'N/A':
                human_val = 'N/A'
            if ft_val == 'N/A':
                na_counts[col] += 1
                discrepancies.append({
                    'Problem': row['Problem'],
                    'Column': col,
                    'Raw_Human': row[f"{col}_human"],
                    'Raw_Model': raw_model_output,
                    'Human': human_val,
                    'Model': 'N/A'
                })
                continue
            if pd.isna(ft_val):
                ft_val = 'N/A'
                na_counts[col] += 1
                discrepancies.append({
                    'Problem': row['Problem'],
                    'Column': col,
                    'Raw_Human': row[f"{col}_human"],
                    'Raw_Model': raw_model_output,
                    'Human': human_val,
                    'Model': 'N/A'
                })
                continue

            # Compare values
            try:
                human_val = str(human_val)
                ft_val = str(ft_val)
                if human_val == ft_val:
                    correct_count += 1
                else:
                    discrepancies.append({
                        'Problem': row['Problem'],
                        'Column': col,
                        'Raw_Human': row[f"{col}_human"],
                        'Raw_Model': raw_model_output,
                        'Human': human_val,
                        'Model': ft_val
                    })
            except Exception as e:
                logging.error(f"Error comparing {col} for problem '{row['Problem'][:30]}': {str(e)}")

        accuracy = (correct_count / len(available_columns)) * 100 if available_columns else 0
        problem_accuracy.append({
            'Problem': row['Problem'],
            'Accuracy': accuracy,
            'Correct_Count': correct_count,
            'Total_Columns': len(available_columns)
        })
        total_correct += correct_count
        total_comparisons += len(available_columns)

    # Calculate overall accuracy
    overall_accuracy = (total_correct / total_comparisons) * 100 if total_comparisons > 0 else 0

    # Create accuracy DataFrame
    problem_accuracy_df = pd.DataFrame(problem_accuracy)
    overall_row = pd.DataFrame([{
        'Problem': 'Overall Accuracy',
        'Total_Columns': total_comparisons,
        'Correct_Count': total_correct,
        'Accuracy': overall_accuracy,
    }])
    problem_accuracy_df = pd.concat([problem_accuracy_df, overall_row], ignore_index=True)
    problem_accuracy_df['Accuracy'] = problem_accuracy_df['Accuracy'].map('{:.3f}'.format)

    # Save accuracy results
    accuracy_output_file = "accuracy_results_fine_tuned.xlsx"
    with pd.ExcelWriter(accuracy_output_file, engine='openpyxl') as writer:
        problem_accuracy_df.to_excel(writer, sheet_name='Accuracy', index=False)
        workbook = writer.book
        worksheet = writer.sheets['Accuracy']
        for cell in worksheet['A'][1:]:
            cell.alignment = Alignment(horizontal='left')
        worksheet.column_dimensions['A'].width = 50
        worksheet.column_dimensions['B'].width = 15
        worksheet.column_dimensions['C'].width = 15
        worksheet.column_dimensions['D'].width = 15
    console.print(f"[bold yellow]Accuracy results saved to {accuracy_output_file}[/bold yellow]")

    # Save discrepancy results
    df_discrepancies = pd.DataFrame(discrepancies)
    console.print(f"[bold yellow]Found {len(df_discrepancies)} discrepancies (including N/A)[/bold yellow]")
    discrepancy_output_file = "discrepancies_fine_tuned.xlsx"
    df_discrepancies.to_excel(discrepancy_output_file, index=False)
    console.print(f"[bold yellow]Discrepancies saved to {discrepancy_output_file}[/bold yellow]")

    # Count discrepancies and N/A frequencies
    discrepancy_counts = df_discrepancies[df_discrepancies['Model'] != 'N/A']['Column'].value_counts().to_dict()
    console.print("\n[yellow]Discrepancy frequency by criterion (excluding N/A):[/yellow]")
    print(discrepancy_counts)
    console.print("\n[yellow]N/A frequency by criterion:[/yellow]")
    print(na_counts)

    # Sample verification: 10% of discrepancies/N/A
    if len(df_discrepancies) > 0:
        sample_size = max(1, len(df_discrepancies) // 10)
        df_sample = df_discrepancies.sample(n=sample_size, random_state=42)
        console.print(f"\n[yellow]Sampling {sample_size} discrepancy/N/A records for verification:[/yellow]")
        for _, row in df_sample.iterrows():
            console.print(
                f"Problem: {row['Problem'][:30]}...\n"
                f"Column: {row['Column']}\n"
                f"Raw Human: {row['Raw_Human']}\n"
                f"Raw Model: {row['Raw_Model'][:50]}...\n"
                f"Formatted Human: {row['Human']}\n"
                f"Formatted Model: {row['Model']}"
            )

    # Print accuracy summary
    console.print(f"\n[bold yellow]Total problems: {len(df_merged)}[/bold yellow]")
    console.print(f"[bold yellow]Overall accuracy: {overall_accuracy:.3f}%[/bold yellow]")
    console.print("\n[bold yellow]Per-problem accuracy:[/bold yellow]")
    print(problem_accuracy_df)

    return problem_accuracy_df, df_discrepancies

