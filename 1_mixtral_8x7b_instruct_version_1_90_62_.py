def compare_prompting_results(model_eval_file: str, output_prefix: str) -> float:
    """Compare the accuracy of different prompting methods with human evaluation"""
    console = Console()

    df_human = pd.read_excel("MWP evaluation sheet.xlsx")

    df_model = pd.read_excel(model_eval_file)

    if 'Section' in df_human.columns and 'Question Type' not in df_human.columns:
        df_human.rename(columns={'Section': 'Question Type'}, inplace=True)

    # Clean the Problem field (remove spaces + convert to lowercase)
    df_human['Problem'] = df_human['Problem'].str.strip().str.lower()
    df_model['Problem'] = df_model['Problem'].str.strip().str.lower()

    # Merge datasets (left join to retain all human-evaluated problems)
    df_merged = pd.merge(
        df_human,
        df_model,
        on='Problem',
        how='left',
        suffixes=('_human', '_model')
    )

    # Dynamically determine common criteria
    common_criteria = [
        col for col in Config.CRITERIA
        if col in df_human.columns and col in df_model.columns
    ]

    # Calculate accuracy rate
    total_correct = 0
    total_comparisons = 0
    problem_accuracy = []

    for idx, row in df_merged.iterrows():
        correct_count = 0
        for col in common_criteria:
            human_val = str(row[f"{col}_human"]).strip()
            model_val = str(row.get(f"{col}_model", "N/A")).strip()

            if human_val == model_val:
                correct_count += 1

        accuracy = (correct_count / len(common_criteria)) * 100 if common_criteria else 0
        problem_accuracy.append({
            'Problem': row['Problem'],
            'Accuracy': accuracy,
            'Correct_Count': correct_count,
            'Total_Columns': len(common_criteria)
        })
        total_correct += correct_count
        total_comparisons += len(common_criteria)

    # Calculate overall accuracy rate
    overall_accuracy = (total_correct / total_comparisons) * 100 if total_comparisons > 0 else 0

    # Save results
    accuracy_df = pd.DataFrame(problem_accuracy)
    accuracy_output = f"accuracy_results_{output_prefix}.xlsx"
    accuracy_df.to_excel(accuracy_output, index=False)
    console.print(f"[bold green]Saved {accuracy_output}[/bold green]")

    return overall_accuracy

