def build_prompt(case, shot_type):
    """Build a formatted prompt for evaluating math word problems
    Constructs a prompt based on the specified shot type and case details."""
    criteria_list = "\n".join([f"{i+1}. {c}: 0 if {d}" for i, (c, d) in enumerate([
        ("Co-reference issues", "pronouns/references are unclear (e.g., 'he' without context)"),
        ("Grammatical errors", "there are grammar mistakes"),
        ("Misspellings", "there are spelling errors"),
        ("Incomplete sentences", "a sentence is incomplete"),
        ("Unsolvable problems", "missing info prevents solving (e.g., no quantity given)"),
        ("Unrealistic", "the scenario is implausible (e.g., a man with 14 eyes)"),
        ("Unit issues", "units are incorrect/inconsistent (e.g., length in grams)"),
        ("Topic safety", "violent/inappropriate (e.g., murder references)"),
        ("Not satisfying grade requirement", "too hard/easy for the grade"),
        ("Not satisfying section requirement", "doesn’t match the section (e.g., addition vs subtraction)"),
        ("Trivial", "too simple for the grade"),
        ("Not a word problem", "just an equation, no narrative")
    ])])

    config = PROMPT_CONFIGS[shot_type]
    return config["template"].format(
        criteria_list=criteria_list,
        example=config["example"] or "",
        grade=case["grade"],
        question_type=case["question_type"],
        problem=case["problem"]
    )

def run_prompting(model, tokenizer, shot_type, test_cases=TEST_CASES):
    """Run prompting for math word problem evaluation
    Generates model outputs for test cases and saves results to a file."""
    pipe = pipeline(
        "text-generation",
        model=model,
        tokenizer=tokenizer,
        device_map="auto"
    )
    generation_config = {
        "max_new_tokens": 256,
        "temperature": 0.2,
        "top_k": 40,
        "top_p": 0.95,
        "do_sample": True,
        "repetition_penalty": 1.5
    }

    results = []
    output_file = f"prompting_results_{shot_type}.txt"
    try:
        with open(output_file, "w", encoding="utf-8") as f:
            for case in test_cases:
                try:
                    prompt = build_prompt(case, shot_type)
                    output = pipe(prompt, **generation_config)[0]['generated_text']
                    formatted_output = process_output(output, case["problem"])
                    results.append({
                        "problem": case["problem"],
                        "grade": case["grade"],
                        "question_type": case["question_type"],
                        "output": formatted_output
                    })
                    log = f"Problem: {case['problem']}\nGrade: {case['grade']}\nQuestion Type: {case['question_type']}\nModel Output:\n{formatted_output}\n{'-'*50}\n"
                    print(log)
                    f.write(log)
                except Exception as e:
                    error_log = f"Error processing case {case['problem']}: {str(e)}\n"
                    print(error_log)
                    f.write(error_log)
        print(f"Results for {shot_type} saved to {output_file}")
    except Exception as e:
        print(f"Failed to process {shot_type} prompting: {str(e)}")
    return results

