def prepare_data(df_final, random_seed=42):
    """Split dataset into training, validation, and test sets
    Uses stratified splitting to maintain grade distribution across subsets."""
    df_train, temp_data = train_test_split(
        df_final, test_size=0.2, stratify=df_final["Grade"], random_state=random_seed
    )
    df_val, df_test = train_test_split(
        temp_data, test_size=0.5, stratify=temp_data["Grade"], random_state=random_seed
    )
    return df_train, df_val, df_test

def format_data(examples):
    """Ensures strict label-criteria alignment
    Formats dataset examples into prompts and labels for training."""
    labels = []
    for i in range(len(examples["Problem"])):
        label_values = [str(examples.get(criterion, 0)[i]) for criterion in CRITERIA]
        label = " ".join(label_values)
        labels.append(label)

    return {
        "prompt": [build_prompt({"grade": examples["Grade"][i], "question_type": examples["Section"][i], "problem": examples["Problem"][i]}, "zero_shot") for i in range(len(examples["Problem"]))],
        "label": labels
    }

