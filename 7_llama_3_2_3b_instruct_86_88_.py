def process_output(raw_output: str, problem: str) -> str:
    """Improved output parser with better pattern matching
    Processes model output to extract evaluation scores and applies fallback rules for missing criteria."""
    pattern = r"(?:^|\n)-?\s*([\w\s]+?)\s*[:：]\s*([01]|True|False|0\.0|1\.0)"
    matches = re.findall(pattern, raw_output, re.IGNORECASE)

    output_map = {}
    for criterion, value in matches:
        criterion = criterion.strip().title()
        criterion = next((c for c in CRITERIA if c.lower() == criterion.lower()), None)
        if criterion:
            output_map[criterion] = '1' if value in ('1', 'True', '1.0') else '0'

    # Fallback values for common errors
    fallback_values = {
        # 1. Co-reference issues: Detects vague pronouns (it/he/she/they) without a clear antecedent in previous context
        "Co-reference issues": '0' if (
            re.search(r'\b(it|he|she|they)\s+(is|are|was|were)\b', problem.lower()) and
            not re.search(r'\b(person|boy|girl|dog|object)\b', problem.lower())
        ) else '1',

        # 2. Grammatical errors: Detects subject-verb disagreement, tense errors, or sentence fragments
        "Grammatical errors": '0' if (
            re.search(r'\b(is|are|was|were)\s+[a-z]+ing\b', problem.lower()) or
            re.search(r'\b(have|has)\s+[a-z]+\s+[a-z]+ed\b', problem.lower()) or
            not re.search(r'[.!?]\s*$', problem.strip())  # Missing sentence-ending punctuation
        ) else '1',

        # 3. Misspellings: Detects common spelling errors using an extended misspelling word list
        "Misspellings": '0' if re.search(
            r'\b(flowrs|yello|catched|recieve|seperate|definately|wierd)\b',
            problem.lower()
        ) else '1',

        # 4. Incomplete sentences: Detects very short phrases or lack of subject-verb structure
        "Incomplete sentences": '0' if (
            len(problem.split()) < 5 or  # Too short
            not re.search(r'\b([A-Z][a-z]+|I)\b.*\b(is|are|has|have|do|did)\b', problem)  # No subject-verb structure
        ) else '1',

        # 5. Unsolvable problems: Detects lack of critical quantitative information
        "Unsolvable problems": '0' if (
            re.search(r'\b(some|a few|several|how (many|much))\b', problem.lower()) and
            not re.search(r'\b\d+\b', problem)
        ) else '1',

        # 6. Unrealistic: Detects absurd or implausible scenarios
        "Unrealistic": '0' if re.search(
            r'\b(14 eyes|murderer|flying elephant|square circle|negative distance)\b',
            problem.lower()
        ) else '1',

        # 7. Unit issues: Detects mixed or invalid units
        "Unit issues": '0' if (
            re.search(r'\b(gram long|area in liters|degrees in meters)\b', problem.lower()) or
            re.search(r'\b\d+\s*(years|months)\s+old\s+\d+\s*(cm|kg)\b', problem.lower())
        ) else '1',

        # 8. Topic safety: Detects sensitive or inappropriate topics
        "Topic safety": '0' if re.search(
            r'\b(murder|kill|gun|drug|abuse|racist|sexist)\b',
            problem.lower()
        ) else '1',

        # 9. Not satisfying grade requirement: Matches topic difficulty with target grade level
        "Not satisfying grade requirement": '0' if (
            re.search(r'\bgrade\s*[1-6]\b', problem.lower()) and
            (
                (re.search(r'\bgrade\s*[1-2]\b', problem.lower()) and re.search(r'\b(algebra|calculus)\b', problem.lower())) or
                (re.search(r'\bgrade\s*[5-6]\b', problem.lower()) and re.search(r'\b(count to 10|basic addition)\b', problem.lower()))
            )
        ) else '1',

        # 10. Not satisfying section requirement: Detects mismatch between problem type and topic category
        "Not satisfying section requirement": '0' if (
            "addition" in problem.lower() and re.search(r'\b(subtract|minus)\b', problem.lower()) or
            "multiplication" in problem.lower() and re.search(r'\b(divided by|ratio)\b', problem.lower())
        ) else '1',

        # 11. Trivial: Detects overly simple or overly difficult problems relative to the grade level
        "Trivial": '0' if (
            (re.search(r'\bgrade\s*[5-6]\b', problem.lower()) and re.search(r'\b(2\+2|count to 5)\b', problem.lower())) or
            (re.search(r'\bgrade\s*[1-2]\b', problem.lower()) and re.search(r'\b(quadratic equation|square root)\b', problem.lower()))
        ) else '1',

        # 12. Not a word problem: Detects pure arithmetic expressions or multiple-choice format
        "Not a word problem": '0' if (
            re.match(r'^\d+\s*[\+\-\*/]\s*\d+\s*=\s*\?*$', problem.strip()) or
            re.search(r'\b(a|b|c|d)\s*[\)\.]\s*', problem.lower())
        ) else '1'
    }


    return '\n'.join(
        f"- {criterion}: {output_map.get(criterion, fallback_values.get(criterion, 'N/A'))}"
        for criterion in CRITERIA
    )
