def compare_prompt_results(shot_type, human_file="/content/MWP evaluation sheet.xlsx"):
    """Compare prompt results with human evaluation
    Evaluates model outputs against human annotations and computes accuracy."""
    df_human = pd.read_excel(human_file)
    df_human['Problem'] = df_human['Problem'].str.strip().str.lower()

    prompt_file = f"prompting_results_{shot_type}.txt"
    with open(prompt_file, "r", encoding="utf-8") as f:
        content = f.read()

    problem_blocks = content.split('-'*50)
    table_data = []
    for block in problem_blocks:
        if not block.strip():
            continue
        problem = re.search(r"Problem: (.+)", block).group(1).strip()
        output = re.search(r"Model Output:\n(.+)", block, re.DOTALL).group(1).strip()

        scores = {}
        for line in output.split('\n'):
            if ': ' in line:
                criterion, value = line.split(': ')
                criterion = criterion.replace('- ', '').strip()
                scores[criterion] = value.strip()

        row = {"Problem": problem}
        for criterion in CRITERIA:
            row[criterion] = scores.get(criterion, "N/A")
        table_data.append(row)

    df_prompt = pd.DataFrame(table_data, columns=["Problem"] + CRITERIA)
    df_prompt['Problem'] = df_prompt['Problem'].str.strip().str.lower()

    df_merged = pd.merge(df_human, df_prompt, on='Problem', how='inner', suffixes=('_human', '_prompt'))

    problem_accuracy = []
    total_correct = total_comparisons = 0
    available_columns = [col for col in CRITERIA if f"{col}_human" in df_merged.columns]

    for _, row in df_merged.iterrows():
        correct_count = 0
        for col in available_columns:
            human_val = row[f"{col}_human"]
            prompt_val = row[f"{col}_prompt"]
            if pd.isna(human_val) or pd.isna(prompt_val):
                continue
            try:
                human_val = str(int(float(human_val))) if '.' in str(human_val) else str(int(human_val))
                prompt_val = str(int(float(prompt_val))) if '.' in str(prompt_val) else str(int(prompt_val))
            except:
                human_val = str(human_val)
                prompt_val = str(str(prompt_val))
            if human_val == prompt_val:
                correct_count += 1
        accuracy = (correct_count / len(available_columns)) * 100 if available_columns else 0
        problem_accuracy.append({
            'Problem': row['Problem'],
            'Accuracy': accuracy,
            'Correct_Count': correct_count,
            'Total_Columns': len(available_columns)
        })
        total_correct += correct_count
        total_comparisons += len(available_columns)

    overall_accuracy = (total_correct / total_comparisons) * 100 if total_comparisons > 0 else 0
    problem_acc_df = pd.DataFrame(problem_accuracy)
    overall_row = pd.DataFrame([{
        'Problem': 'Overall Accuracy',
        'Total_Columns': total_comparisons,
        'Correct_Count': total_correct,
        'Accuracy': overall_accuracy
    }])
    problem_acc_df = pd.concat([problem_acc_df, overall_row], ignore_index=True)

    return problem_acc_df

def process_all_shots():
    """Process all shot types and generate a combined report
    Aggregates accuracy results for zero-shot, one-shot, and three-shot prompting."""
    shots = ['zero_shot', 'one_shot', 'three_shot']
    all_results = []

    for shot in shots:
        try:
            df = compare_prompt_results(shot)
            df.insert(0, 'Shot Type', shot.split('_')[0])
            all_results.append(df)
        except Exception as e:
            print(f"Error processing {shot}: {str(e)}")
            continue

    # Merge results
    combined_df = pd.concat(all_results, ignore_index=True)

    # Data validation
    if combined_df.empty:
        raise RuntimeError("No valid comparison results generated")

    # Save to a single file
    with pd.ExcelWriter("all_shot_accuracy.xlsx", engine='openpyxl') as writer:
        combined_df.to_excel(writer, sheet_name='All Results', index=False)

        worksheet = writer.sheets['All Results']
        worksheet.column_dimensions['A'].width = 12
        worksheet.column_dimensions['B'].width = 50

    # Print accuracy report
    overall_df = combined_df[combined_df['Problem'] == 'Overall Accuracy']
    print("\nAccuracy Rates:")
    for _, row in overall_df.iterrows():
        print(f"{row['Shot Type']}-shot: {row['Accuracy']:.1f}%")

    return combined_df

