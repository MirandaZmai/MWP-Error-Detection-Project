def train_model(model, tokenizer, train_dataset, val_dataset):
    """Fine-tune the model with LoRA and training arguments
    Trains the model on the prepared dataset and visualizes loss curves."""
    lora_config = LoraConfig(
        r=16,
        lora_alpha=64,
        target_modules=["q_proj", "k_proj", "v_proj", "o_proj"],
        lora_dropout=0.1,
        bias="none",
        task_type="CAUSAL_LM"
    )

    model = get_peft_model(model, lora_config)
    model.config.use_cache = False

    training_args = TrainingArguments(
        output_dir="./results",
        num_train_epochs=3,
        eval_strategy="steps",
        save_strategy="steps",
        per_device_train_batch_size=8,
        per_device_eval_batch_size=8,
        gradient_accumulation_steps=2,
        logging_steps=10,
        save_steps=50,
        eval_steps=50,
        warmup_steps=100,
        weight_decay=0.01,
        learning_rate=2e-5,
        optim="adamw_torch_fused",
        warmup_ratio=0.1,
        fp16=True,
        load_best_model_at_end=True,
        metric_for_best_model="loss",
        report_to="none",
        save_safetensors=False,
    )

    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=train_dataset,
        eval_dataset=val_dataset,
        data_collator=DataCollatorForLanguageModeling(tokenizer, mlm=False)
    )

    trainer.train()

    save_path = "./fine_tuned_model"
    os.makedirs(save_path, exist_ok=True)
    model.save_pretrained(save_path, safe_serialization=False)
    tokenizer.save_pretrained(save_path)

    required_files = ["adapter_model.bin", "adapter_config.json"]
    missing = [f for f in required_files if not os.path.exists(os.path.join(save_path, f))]

    if not missing:
        print("✅ Adapter saved successfully")
    else:
        print("❌ Adapter saving failed")
        raise RuntimeError(f"Missing files: {missing}")

    log_history = trainer.state.log_history
    train_loss = [log['loss'] for log in log_history if 'loss' in log and 'eval_loss' not in log]
    val_loss = [log['eval_loss'] for log in log_history if 'eval_loss' in log]
    train_steps = [log['step'] for log in log_history if 'loss' in log and 'eval_loss' not in log]
    val_steps = [log['step'] for log in log_history if 'eval_loss' in log]
    plt.plot(train_steps, train_loss, label='Training Loss')
    plt.plot(val_steps, val_loss, label='Validation Loss', marker='o', markersize=4)
    plt.xlabel('Training Steps')
    plt.ylabel('Loss')
    plt.legend()
    plt.grid(True)
    plt.show()

    return model

