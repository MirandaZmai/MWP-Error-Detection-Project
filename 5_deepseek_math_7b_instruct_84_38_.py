def load_model_and_tokenizer(model_name="deepseek-ai/deepseek-math-7b-instruct"):
    def print_mem(label=""):
        print(f"{label}GPU Mem: Alloc {torch.cuda.memory_allocated()/1024**3:.2f}GB | "
              f"Reserv {torch.cuda.memory_reserved()/1024**3:.2f}GB")

    print_mem("Initial ")

    quant_config = BitsAndBytesConfig(
        load_in_4bit=True,
        bnb_4bit_compute_dtype=torch.bfloat16,
        bnb_4bit_quant_type="nf4",
        bnb_4bit_use_double_quant=True
    )

    print_mem("Pre-load ")
    model = AutoModelForCausalLM.from_pretrained(
        model_name, quantization_config=quant_config, torch_dtype=torch.bfloat16,
        trust_remote_code=True, device_map="cuda:0", low_cpu_mem_usage=True
    )
    print_mem("Post-load ")

    tokenizer = AutoTokenizer.from_pretrained(model_name, trust_remote_code=True)
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token
    tokenizer.padding_side = "right"

    print_mem("Final ")
    return model, tokenizer

