def process_output(raw_output: str, problem: str, criteria: list = CRITERIA) -> str | None:
    """
    Processes raw model output to ensure it meets the specified format requirements.

    Args:
        raw_output: Raw model output string.
        problem: The math word problem for logging context.
        criteria: List of evaluation criteria.

    Returns:
        Formatted string with exactly 12 lines in the format "- Criterion: 0/1", or None if parsing fails.
    """
    output_map = {}
    found_binary = False
    for line in raw_output.strip().split('\n'):
        line = line.strip()
        if not line or ':' not in line:
            continue
        try:
            criterion_part, value_part = line.split(':', 1)
            criterion = criterion_part.replace('- ', '').strip()
            if criterion.startswith(tuple(str(i) + '.' for i in range(1, 13))):
                criterion = criterion.split('.', 1)[1].strip()
            value = value_part.strip().split()[0] if value_part.strip() else None
            if criterion in criteria and value in ('0', '1', 'True', 'False', '1.0', '0.0'):
                output_map[criterion] = '1' if value in ('1', 'True', '1.0') else '0'
                found_binary = True
        except (ValueError, IndexError) as e:
            logging.debug(f"Skipping malformed line: {line}, error: {e}")
            continue

    if not found_binary:
        logging.warning(f"No valid binary values found in output for problem: {problem}")
        return None

    if len(output_map) != len(criteria):
        logging.warning(f"Incomplete output: {len(output_map)} criteria found, expected {len(criteria)} for problem: {problem}")

    final_output = []
    for criterion in criteria:
        value = output_map.get(criterion, 'N/A')  # Default to N/A for missing criteria
        final_output.append(f"- {criterion}: {value}")
    return '\n'.join(final_output)

