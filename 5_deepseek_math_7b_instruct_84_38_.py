def compare_evaluations(df_fine_tuned, human_file="/content/MWP evaluation sheet.xlsx"):
    df_human = pd.read_excel(human_file)

    # Diagnostic: Check shapes and problems
    print(f"df_fine_tuned shape: {df_fine_tuned.shape}")
    print(f"df_fine_tuned Problems:\n{df_fine_tuned['Problem'].tolist()}")
    print(f"df_human shape: {df_human.shape}")
    print(f"df_human Problems:\n{df_human['Problem'].tolist()}")
    print(f"df_human columns:\n{df_human.columns.tolist()}")
    missing_criteria = [c for c in CRITERIA if c not in df_human.columns]
    print(f"Missing criteria in df_human:\n{missing_criteria}")

    # Normalize Problem column
    df_human['Problem'] = df_human['Problem'].str.strip().str.lower()
    df_fine_tuned['Problem'] = df_fine_tuned['Problem'].str.strip().str.lower()

    # Drop rows with missing Problem values
    df_human = df_human.dropna(subset=['Problem'])
    df_fine_tuned = df_fine_tuned.dropna(subset=['Problem'])

    # Merge DataFrames
    df_merged = pd.merge(df_human, df_fine_tuned, on='Problem', how='inner', suffixes=('_human', '_ft'))

    # Diagnostic: Check merge result
    print(f"df_merged shape: {df_merged.shape}")
    print(f"df_merged Problems:\n{df_merged['Problem'].tolist()}")
    missing_problems = set(df_fine_tuned['Problem']) - set(df_merged['Problem'])
    print(f"Problems missing in df_merged:\n{missing_problems}")

    # Check available columns
    available_columns = [col for col in CRITERIA if f"{col}_human" in df_merged.columns and f"{col}_ft" in df_merged.columns]
    print(f"Available columns for comparison:\n{available_columns}")

    problem_accuracy = []
    total_correct = total_comparisons = 0
    for _, row in df_merged.iterrows():
        correct_count = 0
        for col in available_columns:
            human_val = row[f"{col}_human"]
            ft_val = row[f"{col}_ft"]
            if pd.isna(human_val) or pd.isna(ft_val):
                continue
            try:
                human_val = float(human_val) if str(human_val).replace('.', '').isdigit() else str(human_val)
                ft_val = float(ft_val) if str(ft_val).replace('.', '').isdigit() else str(ft_val)
            except (ValueError, TypeError):
                human_val = str(human_val)
                ft_val = str(ft_val)
            if human_val == ft_val:
                correct_count += 1
        accuracy = (correct_count / len(available_columns)) * 100 if available_columns else 0
        problem_accuracy.append({
            'Problem': row['Problem'],
            'Accuracy': accuracy,
            'Correct_Count': correct_count,
            'Total_Columns': len(available_columns)
        })
        total_correct += correct_count
        total_comparisons += len(available_columns)

    overall_accuracy = (total_correct / total_comparisons) * 100 if total_comparisons > 0 else 0
    problem_acc_df = pd.DataFrame(problem_accuracy)
    overall_row = pd.DataFrame([{
        'Problem': 'Overall Accuracy',
        'Total_Columns': total_comparisons,
        'Correct_Count': total_correct,
        'Accuracy': overall_accuracy
    }])
    problem_acc_df = pd.concat([problem_acc_df, overall_row], ignore_index=True)

    with pd.ExcelWriter("accuracy_results.xlsx", engine='openpyxl') as writer:
        problem_acc_df.to_excel(writer, sheet_name='Accuracy', index=False)
        worksheet = writer.sheets['Accuracy']
        for cell in worksheet['A'][1:]:
            cell.alignment = Alignment(horizontal='left')
        worksheet.column_dimensions['A'].width = 50
        worksheet.column_dimensions['B'].width = 15
        worksheet.column_dimensions['C'].width = 15
        worksheet.column_dimensions['D'].width = 15

    print(f"Total Problems: {len(df_merged)}\nOverall Accuracy: {overall_accuracy:.2f}%")
    print("\nAccuracy by Problem:")
    print(problem_acc_df)

    return problem_acc_df

