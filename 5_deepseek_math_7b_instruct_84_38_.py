def prepare_data(df_final, random_seed=42):
    df_train, temp_data = train_test_split(
        df_final, test_size=0.2, stratify=df_final["Grade"], random_state=random_seed
    )
    df_val, df_test = train_test_split(
        temp_data, test_size=0.5, stratify=temp_data["Grade"], random_state=random_seed
    )
    return Dataset.from_pandas(df_train), Dataset.from_pandas(df_val), Dataset.from_pandas(df_test)

def format_data(examples):
    prompts = []
    labels = []
    for i in range(len(examples["Problem"])):
        prompt = (
            f"Analyze this math problem and output a list of 0s and 1s for these error types: {', '.join(CRITERIA)}.\n"
            f"Problem: {examples['Problem'][i]}\n"
            f"Grade: {examples['Grade'][i]}\n"
            f"Section: {examples['Section'][i]}\n"
            "Output:\n"
        )
        label = " ".join(str(examples.get(col, 0)[i]) for col in CRITERIA)
        prompts.append(prompt)
        labels.append(label)
    return {"prompt": prompts, "label": labels}

def tokenize_function(examples, tokenizer, max_length=256):
    full_texts = [p + l for p, l in zip(examples["prompt"], examples["label"])]
    tokenized = tokenizer(
        full_texts, padding="max_length", truncation=True, max_length=max_length,
        return_tensors="pt", padding_side="right"
    )
    input_ids = tokenized["input_ids"]
    attention_mask = tokenized["attention_mask"]
    prompt_tokenized = tokenizer(
        examples["prompt"], padding="max_length", truncation=True,
        max_length=max_length - 24, return_tensors="pt", padding_side="right"
    )
    labels = torch.full_like(input_ids, -100, dtype=torch.long)
    for i in range(len(examples["prompt"])):
        prompt_len = (prompt_tokenized["attention_mask"][i] == 1).sum().item()
        label_tokens = [int(x) for x in examples["label"][i].strip().split()]
        label_tensor = torch.tensor(label_tokens, dtype=torch.long)
        if prompt_len + len(label_tokens) <= max_length:
            labels[i, prompt_len:prompt_len + len(label_tokens)] = label_tensor
    return {
        "input_ids": input_ids.tolist(),
        "attention_mask": attention_mask.tolist(),
        "labels": labels.tolist()
    }

