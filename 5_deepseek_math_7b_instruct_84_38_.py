def run_prompting(model, tokenizer, shot_type, test_cases=TEST_CASES):
    pipe = pipeline("text-generation", model=model, tokenizer=tokenizer)
    criteria_list = "\n".join([f"{i+1}. {c}: 0 if {d}" for i, (c, d) in enumerate([
        ("Co-reference issues", "pronouns/references are unclear (e.g., 'he' without context)"),
        ("Grammatical errors", "there are grammar mistakes"),
        ("Misspellings", "there are spelling errors"),
        ("Incomplete sentences", "a sentence is incomplete"),
        ("Unsolvable problems", "missing info prevents solving (e.g., no quantity given)"),
        ("Unrealistic", "the scenario is implausible (e.g., a man with 14 eyes)"),
        ("Unit issues", "units are incorrect/inconsistent (e.g., length in grams)"),
        ("Topic safety", "violent/inappropriate (e.g., murder references)"),
        ("Not satisfying grade requirement", "too hard/easy for the grade"),
        ("Not satisfying section requirement", "doesn’t match the section (e.g., addition vs subtraction)"),
        ("Trivial", "too simple for the grade"),
        ("Not a word problem", "just an equation, no narrative")
    ])])

    results = []
    config = PROMPT_CONFIGS[shot_type]
    for case in test_cases:
        prompt = config["template"].format(
            criteria_list=criteria_list,
            example=config["example"] or "",
            grade=case["grade"],
            question_type=case["question_type"],
            problem=case["problem"]
        )
        output = pipe(
            prompt,
            max_new_tokens=250,
            do_sample=shot_type != "zero_shot",
            temperature=0.2 if shot_type == "zero_shot" else 0.7,
            top_k=50,
            top_p=0.95,
            return_full_text=False
        )[0]['generated_text']
        formatted_output = process_output(output, case["problem"])
        if formatted_output is None:
            logging.warning(f"Using default output for problem: {case['problem']}")
            formatted_output = '\n'.join(f"- {criterion}: N/A" for criterion in CRITERIA)
        results.append({
            "problem": case["problem"],
            "grade": case["grade"],
            "question_type": case["question_type"],
            "output": formatted_output
        })
        print(f"Problem: {case['problem']}\nGrade: {case['grade']}\nQuestion Type: {case['question_type']}\nModel Output:\n{formatted_output}\n{'-'*50}")
    return results

