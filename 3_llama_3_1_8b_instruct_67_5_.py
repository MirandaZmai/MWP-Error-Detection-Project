def prepare_data(
    df_train,
    test_size=0.3,
    random_seed=42,
    required_columns=REQUIRED_COLUMNS,
    error_columns=ERROR_CATEGORIES,
    default_values=None,
    fig_size=(12, 6),
    palette=["#4daf4a", "#e41a1c"],
    save_dir="visualizations"
):
    """Prepare and split dataset into train, validation, and test sets, with visualizations."""
    console = Console()
    if default_values is None:
        default_values = {"Problem": "", "Section": "Unknown"}
    column_mapping = {col.lower(): col for col in df_train.columns}
    df_train = df_train.rename(columns={
        k: v for k, v in column_mapping.items()
        if k in [c.lower() for c in required_columns]
    })
    missing_columns = [col for col in required_columns if col not in df_train.columns]
    if missing_columns:
        console.print(f"[yellow]Warning: Missing columns {missing_columns}, filling with default values[/yellow]")
        for col in missing_columns:
            if col in default_values:
                df_train[col] = default_values[col]
            elif col in error_columns:
                df_train[col] = 0
            else:
                df_train[col] = ""
    if "Problem" in df_train.columns:
        df_train["Problem"] = df_train["Problem"].fillna(default_values.get("Problem", "")).astype(str)
    if "Section" in df_train.columns:
        df_train["Section"] = df_train["Section"].fillna(default_values.get("Section", "Unknown")).astype(str)
    if "Grade" in df_train.columns:
        df_train["Grade"] = pd.to_numeric(df_train["Grade"], errors="coerce")
    for col in error_columns:
        if col in df_train.columns:
            invalid_values = df_train[col].dropna().apply(
                lambda x: x not in [0, 1, True, False, "0", "1", "true", "false"]
            )
            if invalid_values.any():
                console.print(f"[yellow]Warning: Invalid values in {col}, keeping as pd.NA[/yellow]")
            df_train[col] = df_train[col].apply(
                lambda x: 1 if x in [1, True, "1", "true"] else 0 if x in [0, False, "0", "false"] else pd.NA
            ).astype("Int64")
    if len(df_train) < 5:
        raise ValueError("Dataset too small for splitting. Minimum 5 samples required.")
    elif len(df_train) < 20:
        console.print(f"[yellow]Warning: Dataset size is small ({len(df_train)} samples), using 80/10/10 split[/yellow]")
        test_size = 0.2
    if "Grade" not in df_train.columns or df_train["Grade"].nunique() < 2 or df_train["Grade"].isna().any():
        console.print("[yellow]Warning: Using random split due to missing Grade, insufficient unique grades, or NaN values[/yellow]")
        df_train_split, temp_data = train_test_split(df_train, test_size=test_size, random_state=random_seed)
        df_val, df_test = train_test_split(temp_data, test_size=0.5, random_state=random_seed)
    else:
        df_train_split, temp_data = train_test_split(
            df_train, test_size=test_size, stratify=df_train["Grade"], random_state=random_seed
        )
        df_val, df_test = train_test_split(
            temp_data, test_size=0.5, stratify=temp_data["Grade"], random_state=random_seed
        )
    train_dataset = Dataset.from_pandas(df_train_split)
    val_dataset = Dataset.from_pandas(df_val)
    test_dataset = Dataset.from_pandas(df_test)
    console.print("\n[bold]Dataset Split Summary:[/bold]")
    console.print(f"Total samples: {len(df_train):,}")
    console.print(f"Training set: {len(df_train_split):,} ({len(df_train_split)/len(df_train)*100:.1f}%)")
    console.print(f"Validation set: {len(df_val):,} ({len(df_val)/len(df_train)*100:.1f}%)")
    console.print(f"Test set: {len(df_test):,} ({len(df_test)/len(df_train)*100:.1f}%)")
    # Visualization Plot 1
    n_samples = len(df_train_split)
    error_counts = []
    percentages = {}
    valid_error_columns = [col for col in error_columns if col in df_train_split.columns]
    for col in valid_error_columns:
        ones = df_train_split[col].sum()
        zeros = (df_train_split[col] == 0).sum()
        ones_pct = ones / n_samples * 100
        zeros_pct = zeros / n_samples * 100
        error_counts.append({"Error Type": col, "Value": "1 (No Error)", "Count": ones, "Percent": ones_pct})
        error_counts.append({"Error Type": col, "Value": "0 (Error)", "Count": zeros, "Percent": zeros_pct})
        percentages[col] = {"1 (No Error)": ones_pct, "0 (Error)": zeros_pct}
    palette = {"0 (Error)": "#e74c3c", "1 (No Error)": "#2ecc71"}
    plt.figure(figsize=(10, 8))
    ax = sns.barplot(data=pd.DataFrame(error_counts), x="Error Type", y="Count", hue="Value", palette=palette)
    for p in ax.patches:
        height = p.get_height()
        if height > 0:
            ax.text(
                p.get_x() + p.get_width() / 2, height + 0.5,
                f"{height/n_samples*100:.1f}%",
                ha="center", va="bottom", fontsize=10, color="black"
            )
    plt.xticks(rotation=45, ha="right")
    plt.xlabel("Error Category")
    plt.ylabel("Count")
    plt.title("Distribution of 0 and 1 in Each Error Category (Training Set)")
    plt.legend(title="Error Presence")
    plt.grid(axis="y", linestyle="--", alpha=0.7)
    plt.tight_layout()
