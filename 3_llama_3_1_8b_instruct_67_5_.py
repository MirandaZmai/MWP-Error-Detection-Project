class MathProblemEvaluator:
    def __init__(self, model=None, tokenizer=None, model_name="meta-llama/Meta-Llama-3.1-8B-Instruct"):
        """Initialize the evaluator with a model, tokenizer, and text generation pipeline."""
        if model is None or tokenizer is None:
            self.model, self.tokenizer = load_model_and_tokenizer(model_name)
        else:
            self.model = model
            self.tokenizer = tokenizer
        print("Allocated GPU memory after loading quantized model:", torch.cuda.memory_allocated() / 1024**3, "GB")
        print("Reserved GPU memory after loading quantized model:", torch.cuda.memory_reserved() / 1024**3, "GB")
        self.generator = pipeline(
            "text-generation",
            model=self.model,
            tokenizer=self.tokenizer,
            model_kwargs={
                "torch_dtype": torch.bfloat16,
                "eos_token_id": self.tokenizer.eos_token_id,
                "pad_token_id": self.tokenizer.eos_token_id
            },
            temperature=0.2,
            top_p=0.95,
            do_sample=True,
            max_new_tokens=200
        )

    def build_prompt(self, test_case, prompt_config):
        """Construct a prompt for evaluating a math word problem using the given configuration."""
        return prompt_config["template"].format(
            criteria=CRITERIA_GUIDELINES,
            Grade=test_case["Grade"],
            Section=test_case["Section"],
            Problem=test_case["Problem"],
            example=prompt_config["example"] or ""
        )

    def filter_generated_text(self, generated_text):
        """Extract and clean the evaluation output from generated text."""
        start_marker = "**Initial Evaluation Output:**"
        end_marker = "**Final Evaluation Output**"
        start_idx = generated_text.find(start_marker)
        end_idx = generated_text.find(end_marker)
        if start_idx != -1 and end_idx != -1 and end_idx > start_idx:
            filtered_text = generated_text[start_idx + len(start_marker):end_idx].strip()
        else:
            lines = generated_text.split("\n")
            filtered_lines = [
                line for line in lines
                if re.match(r"-\s*[a-zA-Z\s]+?\s*:\s*(1|0|true|false|N/A)", line, re.IGNORECASE)
            ]
            seen = set()
            unique_lines = []
            for line in filtered_lines:
                key = line.split(":")[0].strip().lower()
                if key not in seen:
                    seen.add(key)
                    unique_lines.append(line)
            filtered_text = "\n".join(unique_lines).strip()
        return filtered_text if filtered_text else generated_text

    def parse_output(self, generated_text):
        """Parse the generated evaluation text into a dictionary of scores for each criterion."""
        scores = {c: "N/A" for c in ERROR_CATEGORIES}
        generated_text_lower = generated_text.lower()
        criteria_mapping = {
            "coreference issues": "Co-reference issues",
            "co reference issues": "Co-reference issues",
            "coref issues": "Co-reference issues",
            "co-references": "Co-reference issues",
            "reference issues": "Co-reference issues",
            "unsolvabe problems": "Unsolvable problems",
            "unsolvables": "Unsolvable problems",
            "appropriateness of grade": "Not satisfying grade requirement",
            "Grade Appropriateness": "Not satisfying grade requirement",
            "unsolvability": "Unsolvable problems"
        }
        for criterion in ERROR_CATEGORIES:
            criterion_lower = criterion.lower()
            pattern = re.compile(rf"-\s*{re.escape(criterion_lower)}\s*:\s*([01]|true|false|n/a)", re.IGNORECASE)
            match = pattern.search(generated_text_lower)
            if match:
                value = match.group(1).lower()
                scores[criterion] = 1 if value in ["1", "true"] else 0 if value in ["0", "false"] else "N/A"
        for raw_name, canonical_name in criteria_mapping.items():
            if scores[canonical_name] != "N/A":
                continue
            pattern = re.compile(rf"-\s*{re.escape(raw_name)}\s*:\s*([01]|true|false|N/A)", re.IGNORECASE)
            match = pattern.search(generated_text)
            if match:
                value = match.group(1).lower()
                scores[canonical_name] = 1 if value in ["1", "true"] else 0 if value in ["0", "false"] else "N/A"
        return scores

    def evaluate_single(self, test_case, prompt_config):
        """Evaluate a single math word problem and return scores and filtered text."""
        try:
            prompt = self.build_prompt(test_case, prompt_config)
            output = self.generator(
                prompt,
                max_new_tokens=200,
                num_return_sequences=1,
                return_full_text=False
            )
            generated_text = output[0]["generated_text"]
            filtered_text = self.filter_generated_text(generated_text)
            scores = self.parse_output(filtered_text)
            return scores, filtered_text
        except Exception as e:
            logging.error(f"Single evaluation failed for case {test_case['Problem']}: {str(e)}")
            return {c: "N/A" for c in ERROR_CATEGORIES}, ""

    def evaluate_all(self, test_cases, prompt_configs):
        """Evaluate all test cases using multiple prompting strategies and return results."""
        results = {}
        for prompt_name, config in prompt_configs.items():
            logging.info(f"Processing strategy: {prompt_name}")
            prompt_name_mapping = {
                "zero_shot": "Zero_Shot",
                "one_shot": "One_Shot",
                "three_shot": "Three_Shot"
            }
            formatted_name = prompt_name_mapping.get(prompt_name, prompt_name.replace("_", " ").title())
            console.print(f"\n=========================== {formatted_name} ===========================", style="yellow")
            case_results = []
            for i, test_case in enumerate(test_cases):
                scores, filtered_text = self.evaluate_single(test_case, config)
                console.print(f"\nTest Case {i}:")
                console.print(f"Problem: [bold]{test_case['Problem']}[/]")
                console.print(f"Grade: [cyan]{test_case['Grade']}[/]")
                console.print(f"Section: [bold yellow]{test_case['Section']}[/]")
                console.print("Generated Text:")
                console.print(filtered_text)
                console.print("Scores:", scores)
                console.print("==" * 25, style="white")
                case_results.append({
                    "test_case_id": i,
                    "problem": test_case["Problem"],
                    "grade": test_case["Grade"],
                    "section": test_case["Section"],
                    "scores": scores
                })
            results[prompt_name] = case_results
            logging.info(f"Completed strategy {prompt_name}, total cases: {len(case_results)}")
        return results

