def timer(func):
    """Measure and print the execution time of a function."""
    def wrapper(*args, **kwargs):
        start = time.time()
        result = func(*args, **kwargs)
        end = time.time()
        print(f"⏱️ '{func.__name__}' execution time: {end - start:.2f} seconds")
        return result
    return wrapper

