@contextlib.contextmanager
def manage_resources(model=None):
    """Manage GPU memory by cleaning up resources after use."""
    try:
        yield
    finally:
        if model is not None:
            del model
        gc.collect()
        torch.cuda.empty_cache()
        logging.info("Resources cleaned (manage_resources)")

