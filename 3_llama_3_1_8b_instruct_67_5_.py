def calculate_accuracy(df_prompt_results, df_human):
    """Calculate accuracy of model evaluations compared to human evaluations."""
    console = Console()
    df_merged = pd.merge(df_prompt_results, df_human, on='Problem', how='inner')
    strategies = ['zero_shot', 'one_shot', 'three_shot']
    accuracy_results = []
    for strategy in strategies:
        matches = sum((df_merged[f"{strategy}_{col}"] == df_merged[col]).sum()
                     for col in ERROR_CATEGORIES if col in df_human.columns)
        total = len(df_merged) * len([col for col in ERROR_CATEGORIES if col in df_human.columns])
        if total == 0:
            console.print(f"[yellow]Warning: {strategy} has no matching error category[/yellow]")
            accuracy = 0
        else:
            accuracy = matches / total * 100
        accuracy_results.append({
            'Strategy': strategy,
            'Accuracy': accuracy,
            'Matches': matches,
            'Total': total
        })
        for _, row in df_merged.iterrows():
            problem = row['Problem']
            problem_matches = sum(1 for col in ERROR_CATEGORIES
                                if col in df_human.columns and row[f"{strategy}_{col}"] == row[col])
            problem_total = len([col for col in ERROR_CATEGORIES if col in df_human.columns])
            problem_accuracy = problem_matches / problem_total * 100 if problem_total > 0 else 0
            accuracy_results.append({
                'Problem': problem,
                'Strategy': strategy,
                'Accuracy Count': problem_matches,
                'Accuracy': problem_accuracy
            })
    return pd.DataFrame(accuracy_results)

